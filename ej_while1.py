c = 1   # contador, inicializar, ejemplo

while (c <= 20):
    print("Numero: ",c)
    c = c + 1       # contador, incrementa o decrementa una variable en un valor FIJO

f = 30

while (f >= 1):
    print("Para atras: ",f)
    f = f - 1   

